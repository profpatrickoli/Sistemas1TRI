# COMO FAZER UM MENU DE OPÇÕES
print("1 - Opção 1")
print("2 - Opção 2")
print("3 - Opção 3")
opcao = int(input("Digite a opção desejada:"))
if (opcao == 1):
    print("Você escolheu 1")
elif (opcao == 2):
    print("Você escolheu 2")
elif (opcao == 3):
    print("Você escolheu 3")
else:
    print("Opção inválida")